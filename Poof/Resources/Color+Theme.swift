/// For folder Main
extension Color {
    struct Main {
        static let primary = Color("primary")
    }
}

// For Semantic
extension Color {
    struct Semantic {
        static let success = Color("success")
        static let error = Color("error")
    }
}

// For Neutrals
extension Color {
    struct Neutrals {
        static let white = Color("white")
        static let black = Color("black")
    }
}

